"""Stage 1 evaluation on the real LOLA south pole DEM.

Pipeline:
  1. Load the trained crater U-Net checkpoint.
  2. Tile the LOLA DEM (7600 x 7600 @ 80 m/px) into overlapping 256 x 256 patches.
  3. Normalize each tile to uint8 [0, 255] per-tile (matches DeepMoon training
     distribution: global LROC-Kaguya DEM stored as 8-bit grayscale).
  4. Run the U-Net with optional flip TTA, average sigmoid probabilities across
     overlapping patches, threshold at 0.5 to produce a stitched 7600 x 7600
     crater prediction mask.
  5. Compare the stitched prediction to the Robbins-derived ground truth mask
     (ring mode, generated by scripts/project_robbins_to_dem.py).
  6. Compute IoU, Dice, precision, recall at the pixel level.
  7. Save a classification overlay PNG (TP / FN / FP color-coded on a hillshade).

Usage:
    python scripts/crater_eval_lola.py \
        --checkpoint outputs/crater_v1_seed1/best_craterunet_seed1.pt \
        --dem data/raw/lola/LDEM_80S_80MPP_ADJ.TIF \
        --gt-mask data/processed/lola_80mpp_crater_mask.tif \
        --out-dir outputs/crater_eval_lola \
        --tta
"""

from __future__ import annotations

import argparse
import json
import time
from pathlib import Path

import cv2
import numpy as np
import rasterio
import torch
import torch.nn.functional as F

REPO_ROOT = Path(__file__).resolve().parent.parent
TILE = 256
STRIDE = 128  # 50% overlap for smooth stitching


def build_crater_unet() -> torch.nn.Module:
    import segmentation_models_pytorch as smp
    return smp.Unet(
        encoder_name="resnet34",
        encoder_weights=None,  # loaded from checkpoint
        in_channels=1,
        classes=1,
    )


def load_checkpoint(model: torch.nn.Module, path: Path, device: torch.device) -> dict:
    ckpt = torch.load(path, map_location=device, weights_only=False)
    # Stage 2 saved under 'model_state_dict'; Stage 1 (crater notebook) saved under 'model'.
    if isinstance(ckpt, dict):
        state_key = "model_state_dict" if "model_state_dict" in ckpt else "model" if "model" in ckpt else None
        if state_key is None:
            model.load_state_dict(ckpt)
            meta = {}
        else:
            model.load_state_dict(ckpt[state_key])
            meta = {k: v for k, v in ckpt.items() if k != state_key and not isinstance(v, torch.Tensor)}
    else:
        model.load_state_dict(ckpt)
        meta = {}
    model.to(device).eval()
    return meta


def normalize_tile(tile: np.ndarray) -> np.ndarray:
    """Per-tile min-max normalize to float [0, 1]. NaN -> 0."""
    t = np.where(np.isnan(tile), 0.0, tile).astype(np.float32)
    lo, hi = np.percentile(t, [1, 99])
    if hi - lo < 1e-6:
        return np.zeros_like(t)
    return np.clip((t - lo) / (hi - lo), 0.0, 1.0)


@torch.no_grad()
def infer_tile(model: torch.nn.Module, tile01: np.ndarray, device: torch.device, tta: bool) -> np.ndarray:
    """Return sigmoid prob map (H, W) in [0, 1]."""
    x = torch.from_numpy(np.ascontiguousarray(tile01, dtype=np.float32)).unsqueeze(0).unsqueeze(0).to(device)
    if not tta:
        return torch.sigmoid(model(x)).squeeze().cpu().numpy()
    probs = torch.sigmoid(model(x))
    probs = probs + torch.flip(torch.sigmoid(model(torch.flip(x, dims=[3]))), dims=[3])
    probs = probs + torch.flip(torch.sigmoid(model(torch.flip(x, dims=[2]))), dims=[2])
    probs = probs + torch.flip(torch.sigmoid(model(torch.flip(x, dims=[2, 3]))), dims=[2, 3])
    return (probs / 4.0).squeeze().cpu().numpy()


def tile_positions(H: int, W: int, tile: int = TILE, stride: int = STRIDE) -> list[tuple[int, int]]:
    ys = list(range(0, max(H - tile, 0) + 1, stride))
    xs = list(range(0, max(W - tile, 0) + 1, stride))
    if ys and ys[-1] != H - tile:
        ys.append(H - tile)
    if xs and xs[-1] != W - tile:
        xs.append(W - tile)
    return [(y, x) for y in ys for x in xs]


def sliding_inference(
    dem: np.ndarray,
    valid_mask: np.ndarray,
    model: torch.nn.Module,
    device: torch.device,
    tta: bool = True,
) -> np.ndarray:
    H, W = dem.shape
    prob_sum = np.zeros((H, W), dtype=np.float32)
    count = np.zeros((H, W), dtype=np.float32)

    positions = tile_positions(H, W)
    t0 = time.time()
    for i, (y, x) in enumerate(positions):
        tile = dem[y:y + TILE, x:x + TILE]
        vtile = valid_mask[y:y + TILE, x:x + TILE]
        if vtile.sum() == 0:
            continue
        tile01 = normalize_tile(tile)
        p = infer_tile(model, tile01, device, tta=tta)
        prob_sum[y:y + TILE, x:x + TILE] += p
        count[y:y + TILE, x:x + TILE] += 1.0
        if (i + 1) % 200 == 0:
            dt = time.time() - t0
            print(f"  tile {i+1}/{len(positions)} | {dt:.0f}s elapsed")

    avg = np.where(count > 0, prob_sum / np.maximum(count, 1e-6), 0.0)
    return avg


def binary_metrics(pred: np.ndarray, gt: np.ndarray, valid: np.ndarray) -> dict:
    pm = pred[valid].astype(bool)
    gm = gt[valid].astype(bool)
    tp = int((pm & gm).sum())
    fp = int((pm & ~gm).sum())
    fn = int((~pm & gm).sum())
    tn = int((~pm & ~gm).sum())
    inter = tp
    union = tp + fp + fn
    iou = inter / union if union > 0 else 0.0
    dice = 2 * tp / (2 * tp + fp + fn) if (2 * tp + fp + fn) > 0 else 0.0
    prec = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    rec = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    return {
        "iou": iou, "dice": dice, "precision": prec, "recall": rec,
        "tp": tp, "fp": fp, "fn": fn, "tn": tn,
        "pred_coverage": pm.mean() if pm.size else 0.0,
        "gt_coverage": gm.mean() if gm.size else 0.0,
    }


def hillshade(arr: np.ndarray) -> np.ndarray:
    a = np.where(np.isnan(arr), 0, arr).astype(np.float32)
    gy, gx = np.gradient(a)
    slope = np.pi / 2 - np.arctan(np.hypot(gx, gy))
    aspect = np.arctan2(-gx, gy)
    az = np.deg2rad(360 - 315 + 90)
    alt = np.deg2rad(45)
    shade = np.sin(alt) * np.sin(slope) + np.cos(alt) * np.cos(slope) * np.cos(az - aspect)
    shade = np.clip(shade, 0, 1)
    return (shade * 255).astype(np.uint8)


def classification_overlay(dem: np.ndarray, pred: np.ndarray, gt: np.ndarray, max_side: int = 2000) -> np.ndarray:
    scale = min(1.0, max_side / max(dem.shape))
    if scale < 1.0:
        dsize = (int(dem.shape[1] * scale), int(dem.shape[0] * scale))
        dem_s = cv2.resize(dem, dsize, interpolation=cv2.INTER_AREA)
        pred_s = cv2.resize(pred.astype(np.uint8), dsize, interpolation=cv2.INTER_NEAREST)
        gt_s = cv2.resize(gt.astype(np.uint8), dsize, interpolation=cv2.INTER_NEAREST)
    else:
        dem_s, pred_s, gt_s = dem, pred.astype(np.uint8), gt.astype(np.uint8)

    hs = hillshade(dem_s)
    rgb = cv2.cvtColor(hs, cv2.COLOR_GRAY2RGB)
    tp = (pred_s & gt_s).astype(bool)
    fn = (~pred_s.astype(bool)) & gt_s.astype(bool)  # ground truth, model missed
    fp = pred_s.astype(bool) & (~gt_s.astype(bool))  # model-only
    rgb[tp] = [60, 220, 60]     # green
    rgb[fn] = [220, 60, 60]     # red
    rgb[fp] = [80, 140, 255]    # blue
    return rgb


def main() -> None:
    p = argparse.ArgumentParser()
    p.add_argument("--checkpoint", type=Path, required=True)
    p.add_argument("--dem", type=Path, default=REPO_ROOT / "data/raw/lola/LDEM_80S_80MPP_ADJ.TIF")
    p.add_argument("--gt-mask", type=Path, default=REPO_ROOT / "data/processed/lola_80mpp_crater_mask.tif")
    p.add_argument("--out-dir", type=Path, default=REPO_ROOT / "outputs/crater_eval_lola")
    p.add_argument("--threshold", type=float, default=0.5)
    p.add_argument("--tta", action="store_true")
    args = p.parse_args()

    args.out_dir.mkdir(parents=True, exist_ok=True)
    device = torch.device("cuda" if torch.cuda.is_available() else "cpu")
    print(f"Device: {device}  |  TTA: {args.tta}  |  threshold: {args.threshold}")

    # Load model
    model = build_crater_unet()
    meta = load_checkpoint(model, args.checkpoint, device)
    print(f"Checkpoint: {args.checkpoint.name}  epoch {meta.get('epoch', '?')}  best {meta.get('best_metric', '?')}")

    # Load DEM + GT
    with rasterio.open(args.dem) as ds:
        dem = ds.read(1)
        dem_profile = ds.profile
    with rasterio.open(args.gt_mask) as ds:
        gt = ds.read(1).astype(np.uint8)
    if gt.shape != dem.shape:
        raise ValueError(f"DEM {dem.shape} and GT {gt.shape} must match")
    valid = ~np.isnan(dem)

    print(f"DEM: {dem.shape}  valid pixels: {valid.sum() / valid.size * 100:.1f}%  GT coverage: {gt.sum() / gt.size * 100:.2f}%")

    # Sliding inference
    print(f"Running sliding inference ({TILE}x{TILE}, stride {STRIDE})...")
    t0 = time.time()
    prob = sliding_inference(dem, valid, model, device, tta=args.tta)
    print(f"Inference done in {(time.time() - t0) / 60:.1f} min")

    pred = (prob >= args.threshold).astype(np.uint8)
    pred[~valid] = 0

    # Metrics
    metrics = binary_metrics(pred, gt, valid)
    print("\n=== LOLA south pole crater detection ===")
    for k in ("iou", "dice", "precision", "recall"):
        print(f"  {k:<10} {metrics[k]:.4f}")
    for k in ("tp", "fp", "fn", "pred_coverage", "gt_coverage"):
        val = metrics[k]
        if isinstance(val, float):
            print(f"  {k:<10} {val:.4f}")
        else:
            print(f"  {k:<10} {val:,}")

    # Save artifacts
    prob_path = args.out_dir / "prob.tif"
    pred_path = args.out_dir / "pred.tif"
    overlay_path = args.out_dir / "classification_overlay.png"
    metrics_path = args.out_dir / "metrics.json"

    prof = dict(dem_profile); prof.update(dtype="float32", count=1, compress="lzw", nodata=None)
    with rasterio.open(prob_path, "w", **prof) as dst:
        dst.write(prob.astype(np.float32), 1)
    prof.update(dtype="uint8")
    with rasterio.open(pred_path, "w", **prof) as dst:
        dst.write(pred, 1)

    overlay = classification_overlay(dem, pred, gt)
    cv2.imwrite(str(overlay_path), cv2.cvtColor(overlay, cv2.COLOR_RGB2BGR))

    summary = {
        "checkpoint": str(args.checkpoint),
        "checkpoint_meta": {k: (v if isinstance(v, (int, float, str, bool)) else str(v)) for k, v in meta.items()},
        "dem": str(args.dem),
        "gt_mask": str(args.gt_mask),
        "tile": TILE,
        "stride": STRIDE,
        "threshold": args.threshold,
        "tta": args.tta,
        "metrics": metrics,
    }
    metrics_path.write_text(json.dumps(summary, indent=2, default=str))
    print(f"\nArtifacts:")
    print(f"  probability GeoTIFF:    {prob_path}")
    print(f"  binary prediction:      {pred_path}")
    print(f"  classification overlay: {overlay_path}")
    print(f"  metrics JSON:           {metrics_path}")


if __name__ == "__main__":
    main()
